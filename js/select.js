document
  .getElementById("select-player-1")
  .addEventListener("click", function () {
    const playerNumber = getPlayerNumber();
    if (playerNumber === 5) {
      alert("Sorry!! You can not select more than 5 players!!");
      return;
    }
    getPlayer("player-name-1");
    setSelectButton("select-player-1");
  });
